export async function generateMetadata({ params }: BlogPostPageProps): Promise<Metadata> {
  const article = articles[params.slug];
  
  if (!article) {
    return {
      title: "Статья не найдена",
    };
  }

  return {
    title: `${article.title} | grifun.ru`,
    description: article.description,
  };
}

